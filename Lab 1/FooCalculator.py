import calculator as c



class FooCalculator:

    def __init__(self):
        pass


    def sum(self,m,n):
        return c.sum(m,n)


    def divide(self,m,n):
        return c.divide(m,n)


    def substract(self,m,n):
        return c.substract(m,n)

    def multiply(self,m,n):
        return c.multiply(m,n)

    def gcd(self,m,n):
        return c.gcd(m,n)



